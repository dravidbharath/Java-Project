public class ArithmeticOperatorsDemo {
    public static void main(String[] args) {

        byte num1;
        byte num2 = 5;
        byte num3 = 3;
        num1 = (byte)(num2 + num3);
        System.out.println(num1);


        double num5;
        double num6 = 5.26;
        double num7 = 3.36;
        num5 = (double)(num6 + num7);
        System.out.println(num5);

    }
}
